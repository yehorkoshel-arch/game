import { spawn } from "node:child_process";
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const ROOT = process.cwd();
const DEFAULT_VOICE = "uk-UA-OstapNeural";
const DEFAULT_LANG = "uk-UA";
const LINES_PATH = path.join(ROOT, "public", "audio", "voice", "voice-lines.json");
const OUT_DIR = path.join(ROOT, "public", "audio", "voice");
const MANIFEST_PATH = path.join(ROOT, "src", "audio", "voiceManifest.js");

const args = new Map();
for (let i = 2; i < process.argv.length; i++) {
  const arg = process.argv[i];
  if (arg.startsWith("--")) {
    const key = arg.slice(2);
    const next = process.argv[i + 1];
    if (!next || next.startsWith("--")) args.set(key, true);
    else {
      args.set(key, next);
      i++;
    }
  }
}

const voice = String(args.get("voice") || process.env.VOICE || DEFAULT_VOICE);
const lang = String(args.get("lang") || process.env.LANG_CODE || DEFAULT_LANG);
const group = args.get("group") ? String(args.get("group")) : "";
const ids = args.get("id")
  ? String(args.get("id"))
      .split(",")
      .map((id) => id.trim())
      .filter(Boolean)
  : [];
const limit = args.get("limit") ? Number(args.get("limit")) : 0;
const force = Boolean(args.get("force"));

function normalizeSpeechText(text) {
  return String(text || "")
    .replace(/Роботрон-9000/g, "Роботрон девʼять тисяч")
    .replace(/Роботрон девять тисяч/g, "Роботрон девʼять тисяч")
    .replace(/ТЦК/g, "те це ка")
    .replace(/Києве/g, "Києве")
    .replace(/Слава Україні/g, "Слава Україні")
    .replace(/УРА/g, "Ура")
    .replace(/ПЕРЕМОГА/g, "Перемога")
    .replace(/[.,!?;:…"'“”„«»()[\]{}<>—–-]/g, " ")
    .replace(/[ʼ'`]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function prepareTextForTts(text) {
  return String(text || "")
    .replace(/[—–]/g, ", ")
    .replace(/\s+/g, " ")
    .trim();
}

function run(command, commandArgs) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, commandArgs, {
      cwd: ROOT,
      shell: process.platform === "win32",
      stdio: "inherit",
    });
    child.on("exit", (code) => {
      if (code === 0) resolve();
      else reject(new Error(`${command} exited with ${code}`));
    });
    child.on("error", reject);
  });
}

async function exists(filePath) {
  try {
    await readFile(filePath);
    return true;
  } catch {
    return false;
  }
}

function toManifest(lines) {
  const entries = lines
    .map((line) => {
      const key = normalizeSpeechText(line.text);
      const value = `/game/audio/voice/${line.id}.mp3`;
      return `  ${JSON.stringify(key)}: ${JSON.stringify(value)},`;
    })
    .join("\n");

  return `// Generated by scripts/generate-voice.mjs.\n// Keys are normalized Ukrainian phrases after normalizeSpeechText().\nexport const VOICE_CLIPS = {\n${entries}\n};\n`;
}

let lines = JSON.parse(await readFile(LINES_PATH, "utf8"));
if (group) lines = lines.filter((line) => line.group === group);
if (ids.length > 0) lines = lines.filter((line) => ids.includes(line.id));
if (limit > 0) lines = lines.slice(0, limit);

console.log(`Generating ${lines.length} voice files`);
console.log(`Voice: ${voice}`);
console.log(`Language: ${lang}`);

const generated = [];
for (const line of lines) {
  const outFile = path.join(OUT_DIR, `${line.id}.mp3`);
  if (!force && (await exists(outFile))) {
    console.log(`Skip existing ${line.id}.mp3`);
    generated.push(line);
    continue;
  }
  console.log(`Generate ${line.id}.mp3`);
  await run("npx", [
    "--yes",
    "node-edge-tts@1.2.10",
    "--text",
    prepareTextForTts(line.text),
    "--filepath",
    outFile,
    "--voice",
    voice,
    "--lang",
    lang,
  ]);
  generated.push(line);
}

await writeFile(MANIFEST_PATH, toManifest(generated), "utf8");
console.log(`Updated ${path.relative(ROOT, MANIFEST_PATH)}`);
